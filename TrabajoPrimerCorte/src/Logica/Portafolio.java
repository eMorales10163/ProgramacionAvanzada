package Logica;

public class Portafolio {
	
	String objetivo;
	String añoDeCreacion;
	String numeroCreacion;
	public String getObjetivo() {
		return objetivo;
	}
	public void setObjetivo(String objetivo) {
		this.objetivo = objetivo;
	}
	public String getAñoDeCreacion() {
		return añoDeCreacion;
	}
	public void setAñoDeCreacion(String añoDeCreacion) {
		this.añoDeCreacion = añoDeCreacion;
	}
	public String getNumeroCreacion() {
		return numeroCreacion;
	}
	public void setNumeroCreacion(String numeroCreacion) {
		this.numeroCreacion = numeroCreacion;
	}
	
	

}
